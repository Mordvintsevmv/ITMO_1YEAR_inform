'''
    Работа с целыми числами 

'''

a = 17
b = 13

print("a = ", a)  # Вывод значения a
print('b = ', b)  # Вывод значения b
print('a + b = ', a + b)  # Сложение
print('a - b = ', a - b)  # Вычитание
print('a * b = ', a * b)  # Умножение
print('a / b = ', a / b)  # Деление
print('a // b = ', a // b)  # Округление вниз до целого (деление)
print('a % b = ', a % b)  # Остаток от деления
print('a ** b = ', a ** b)  # Возведение в степень

# Приведение типа int к float

a1 = float(a)
print('a1 =', a1)
print('\n\n')

